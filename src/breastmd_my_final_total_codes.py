def calculate_metrics(logits, target, eps=1e-6):

    pred = torch.sigmoid(logits)

    pred = (pred > 0.5).float()

    tp = (pred * target).sum(dim=(1,2,3))

    fp = (
        pred * (1-target)
    ).sum(dim=(1,2,3))

    fn = (
        (1-pred) * target
    ).sum(dim=(1,2,3))

    dsc = (
        2*tp + eps
    ) / (
        2*tp + fp + fn + eps
    )

    iou = (
        tp + eps
    ) / (
        tp + fp + fn + eps
    )

    precision = (
        tp + eps
    ) / (
        tp + fp + eps
    )

    recall = (
        tp + eps
    ) / (
        tp + fn + eps
    )

    return (

        dsc.mean().item(),

        iou.mean().item(),

        precision.mean().item(),

        recall.mean().item()
    )

